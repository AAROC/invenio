    reclist = []
    reclist_addend = []

    for recID in lrecIDs:
        if rnkdict.has_key(recID):
            reclist.append((recID, rnkdict[recID][1]))
            del rnkdict[recID]
        else:
            reclist_addend.append((recID, 0))
    reclist.sort(lambda x, y: cmp(x[1], y[1]))
